/**
 * op_add - divides @a by @b
 *
 * @a: input 1
 * @b: input 2
 *
 * Return: results of the division @a and @b
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - divides @a by @b
 *
 * @a: input 1
 * @b: input 2
 *
 * Return: results of the division @a and @b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

